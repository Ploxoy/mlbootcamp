  my.extendedColsTrain(XL, function(XL, newdata=NULL) {
    my.normalizedTrain(XL, function (XL, newdata=NULL) {
      my.train.xgb(XL, params, newdata)
    }, newdata=newdata)
  }, idxes=xeee, pairs=xppp, angles=T, x11=T, x11bin=F, newdata=newdata)
}

